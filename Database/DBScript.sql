CREATE PROCEDURE [dbo].[Stock_InsertStock]
	@Quantity varchar(200),
	@PRICE FLOAT,
	@CREATEDON VARCHAR(50),
	@STOCKNAME VARCHAR(200),
	@LICENSEYEAR VARCHAR(50),
	@DISTRIBUTOR VARCHAR(200),
	@DESCRIPTION VARCHAR(MAX),
	@COLOR VARCHAR(50)
AS
BEGIN
	DECLARE @IDSTOCK TABLE (IDSTOCK	 INT)
	INSERT INTO Stock
	output inserted.StockID INTO @IDSTOCK
	VALUES(@Quantity,@PRICE,1,@CREATEDON,@CREATEDON)

	INSERT INTO StockDetail
	SELECT IDSTOCK,
			@STOCKNAME,
			@LICENSEYEAR,
			@DISTRIBUTOR,
			@DESCRIPTION,
			@COLOR,
			@CREATEDON,
			@CREATEDON
	FROM @IDSTOCK

	SELECT IDSTOCK FROM @IDSTOCK
END
GO
