CREATE PROCEDURE [dbo].[Stock_UPDATEStock]
	@ID VARCHAR(50),
	@Quantity varchar(200),
	@PRICE FLOAT,
	@CREATEDON VARCHAR(50),
	@STOCKNAME VARCHAR(200),
	@LICENSEYEAR VARCHAR(50),
	@DISTRIBUTOR VARCHAR(200),
	@DESCRIPTION VARCHAR(MAX),
	@COLOR VARCHAR(50)
AS
BEGIN

	UPDATE  Stock
	SET Quantity=@Quantity,
		PRICE=@PRICE,
		UpdatedOn=@CREATEDON
	WHERE STOCKID=@ID

	UPDATE StockDetail
	SET STOCKNAME=@STOCKNAME,
		LICENSEYEAR=@LICENSEYEAR,
		DISTRIBUTOR=@DISTRIBUTOR,
		DESCRIPTION=@DESCRIPTION,
		COLOR=@COLOR,
		UPDATEDON=@CREATEDON
	WHERE IDSTOCK=@ID

END
